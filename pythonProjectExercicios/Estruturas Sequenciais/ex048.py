#48. Faça um programa que receba dois números, calcule e mostre a divisão do primeiro pelo segundo. Sabe-se que o
# segundo número não pode ser zer, portanto, não é necessário se preocupar com validações.

n1 = int(input("Primeiro Número: "))
n2 = int(input("Segundo Número: "))
print(f"{n1} / {n2} = {n1 / n2}")
