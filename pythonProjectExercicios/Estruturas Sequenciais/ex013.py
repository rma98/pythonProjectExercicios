#13. Calcular a média de 4 números digitados pelo usuário

num1 = float(input("Número 1: "))
num2 = float(input("Número 2: "))
num3 = float(input("Número 3: "))
num4 = float(input("Número 4: "))
print(f"Média: {(num1 + num2 + num3 + num4) / 4:.1f}")

#13.1 Desenvolva um programa que leia as duas notas de um aluno, calcule e mostre a sua média.

nota1 = float(input('Nota 1: '))
nota2 = float(input('Nota 2: '))
media = (nota1 + nota2) / 2
print(f'Média: {media:.1f}')
